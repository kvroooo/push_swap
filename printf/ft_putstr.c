#include "ft_printf.h"

int	ft_putstr(const char *str)
{
	int	i;
	int	len;

	i = 0;
	len = 0;
	if (str == NULL)
		return (write(1, "(null)", 6));
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
		len++;
	}
	return (len);
}
